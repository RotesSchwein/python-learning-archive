for i in range(10):
    print(f"첫 번째 for loop의 시작입니다. i는 {i}")
    for j in range(10):
        print("두 번째 for loop의 시작입니다.")
        print(f"j는 {j} 입니다.")
        print("두 번째 for loop의 끝입니다.")

    print("첫 번째 for loop의 끝입니다.")

print("여기는 for loop가 아닙니다")

